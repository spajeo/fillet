#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ptr;

	ptr = malloc(sizeof(void) * size);
	if (ptr == NULL)
		return (0);
	ft_bzero(ptr, size);
	return (ptr);
}
